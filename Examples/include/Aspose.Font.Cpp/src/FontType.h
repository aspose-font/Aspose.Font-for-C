namespace Aspose {

namespace Font {

/// <summary>
/// Specifies Font type. 
/// </summary>
enum class FontType
{
    /// <summary>
    /// TTF (TrueType) Font type and related.
    /// </summary>
    TTF,
    /// <summary>
    /// Type1 Font type.
    /// </summary>
    Type1,
    /// <summary>
    /// CFF (Compact font format) Font type.
    /// </summary>
    CFF,
    /// <summary>
    /// OpenType Font.
    /// OpenType Font format is an extension of the TrueType Font format, adding support for PostScript Font data.
    /// </summary>
    OTF
};

} // namespace Font
} // namespace Aspose


